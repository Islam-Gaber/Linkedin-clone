    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function view(User $user, Post $post)
    {
        $connection = Connection::where(function ($query) use ($user, $post) {
            $query->where('user_id', $user->id)
                ->where('connection_id', $post->user_id);
        })->orWhere(function ($query) use ($user, $post) {
            $query->where('user_id', $post->user_id)
                ->where('connection_id', $user->id);
        })->first();

        if ($connection && $connection->status === Connection::STATUS_BLOCKED) {
            return false;
        }

        return true;
    }

